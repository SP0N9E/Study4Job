#include <iostream>
#include <vector>
#include <map>
#include <unordered_map>
#include <algorithm>
#include <queue>
#include <set>
#include <unordered_set>
#include <stack>
using namespace std;

struct TreeNode
{
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};

// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    vector<vector<int>> levelOrder(TreeNode* root) {
        vector<vector<int>> res;
        queue<TreeNode*> q;
        if (root!=nullptr)
            q.push(root);

        while (!q.empty())
        {
            int n = q.size();
            vector<int> levelres;
            for (int i = 0; i<n;i++)
            {
                auto node = q.front();
                q.pop();
                levelres.push_back(node->val);
                if (node->left!=nullptr)
                {
                    q.push(node->left);
                }
                if (node->right!=nullptr)
                {
                    q.push(node->right);
                }

            }
            res.push_back(levelres);
        }
        return res;
    }
};
// @lc code=end

